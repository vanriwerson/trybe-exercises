//1.Faça cinco programas, um para cada operação aritmética básica. 
let a = 6
let b = 2;

//Adição
let soma = a + b;
console.log('O resultado da soma é', soma);

// Subtração
let subtracao = a - b;
console.log('O resultado da subtração é', subtracao);

// Multiplicação
let multiplicacao = a * b;
console.log('O resultado da multiplicação é', multiplicacao);

// Divisão (a / b)
let divisao = a / b;
console.log('O resultado da divisão é', divisao);

// Módulo (a % b)
let restoDaDivisao = a % b;
console.log('Oresto da divisão (módulo) é', restoDaDivisao);